       <div class="paragraph">
       <p>
		The Philippine Men and Women of Science (PMWS) is a semi-annual online publication of the Science and Technology Institute (STII).
 	   </p>
       <p>
		First published in 1964 as the Philippine Men of Science (PMS), the first volume contained 101 scientists. Given the number of staff and the manner of data gathering in the pre internet days, questionnaires were disseminated and data was gathered on foot by researchers. The publication was irregular.
 	   </p>
       <p>
		In 2001, the STII developed the systems, the ScINET-PHIL Integrated Library Management System (SILMS) and WebDB (which consist of five databases). One the five databases is the Scientist Database which efficiently process and make the PMS data available to the public.
 	   </p>
       <p>
		PMS was accessible online thru SILMS and WebDB. Since only limited clients can access to these sites, PMS was uploaded to two additional sites (www.science.ph and www.stii.dost.gov.ph).
 	   </p>
       <p>
		On August 28, 2012, PMS Volume 24, June 2012 issue was uploaded to make it more visible and accessible to more users.  A download counter was also installed to monitor usage. PMWS can also be accessed thru science.ph, www.stii.dost.gov.ph and scinet.dost.gov.ph portals.
 	   </p>
       <p>
		STII has unofficially (document change only; official registration in progress) changed PMS to Philippine Men and Women of Science (PMWS) in its online display in response to more gender-sensitive title of the publication. PMWS was adopted online on July 05, 2013 (Volume 26, June 2013 issue).
 	   </p>
       <p>
		Two things stand out looking at the maiden issue, nearly fifty years later: One is that the collection includes women scientists, even before the battle for gender equality was won (While the title publication was entitled Philippine Men of Science, the subtitle acknowledged the number of women who were already made a name for themselves in the branches of S&T); Two, that the publication's goal – for the collection to serve as an inspiration for the youth – still holds true today.
 	   </p>
       <p>
		Then Commissioner Canuto G. Manuel of the National Science Development Board (NSDB), commended the Documentation Division for coming out with the Philippine Men of Science. Commissioner Manuel said, "This volume is indeed a treasury of researches that should guide and propel those who are currently engaged in research or technological activities to greater and more fruitful action, as well as beacon and inspire the youth with potential talents for scientific inquiry to embrace the scientific career in order to hasten our material growth and stability."
 	   </p>
	   <p>	 	
		The publication is a good resource material for researchers who need information on the scientists' academic and professional contributions and affiliations.
 	   </p>
       <p>
		Aside from making the Philippine Men and Women of Science (PMWS) publication accessible and available (free downloads) to all researchers, this site serves as a vehicle to disseminate other information on the Filipino scientists.
 	   </p>
       <p>
		Interviews and announcements on events for the scientists will be posted on this site. The public are also encouraged to comment, suggest and make inquiries regarding events and publications posted on site. Photos of past and present scientists will also be displayed for the Filipinos to get to know these great men and women who are among the unsung heroes of this land.
 	   </p>
       <p>
		The interviews are mainly about the lives of scientists, their interests and other aspirations, apart from their professional contributions which are already featured in the semi-annual publication.       
       </p>
